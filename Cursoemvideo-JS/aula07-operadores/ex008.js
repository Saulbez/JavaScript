/* 
Operadores ternários:
? -> se verdadeiro executa algo
: -> se não, executarar outra coisa
*/

var media = 5.5
var media2 = 8.5

console.log(media > 7? 'Aprovado' : 'Reprovado')
console.log(media2 > 7? 'Aprovado' : 'Reprovado')

var x = 8
var res = x % 2 == 0? 5: 9

console.log(res)

var idade = 18

console.log(idade >= 18? 'Maior' : 'Menor')